void FbdWords::chkLegal(std::string& str)
{
    int cnt = sizeof(gFbdWords) / sizeof(const char*);

    for (int i = 0; i < cnt; i++){
        std::string strOld = gFbdWords[i];
        std::string strNew = "*";
        str = replace_all(str, strOld, strNew);
    }
}

//******************************************************************************
//replace_all
//******************************************************************************
std::string& FbdWords::replace_all(std::string& str,const std::string& old_value,
                                   const std::string& new_value)
{
	while(true) {
		std::string::size_type pos(0);
		if( (pos=str.find(old_value))!=std::string::npos )
			str.replace(pos,old_value.length(),new_value);
		else break;
	}
	return str;
}

//******************************************************************************
// check 没有屏蔽字return true    有屏蔽字 return false
//******************************************************************************
bool FbdWords::CheckLegal(std::string str0)
{
    bool b1 = false;
    
    int cnt = sizeof(gFbdWords) / sizeof(const char*);
    for (int i = 0; i < cnt; i++)
    {
        std::string strOld = gFbdWords[i];
        if (str0.find(strOld) != std::string::npos)
        {
            b1 = true;
            break;
        }
    }
    return b1;
}




